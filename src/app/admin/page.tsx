import { requireServerAdmin } from "@/lib/auth/server-helpers";
import { prisma } from "@/lib/prisma/client";

async function getDashboardData() {
  try {
    const [
      totalUsers,
      adminUsers,
      regularUsers,
      recentUsers,
      usersThisMonth,
      usersLastMonth,
    ] = await Promise.all([
      // Total users
      prisma.user.count(),
      // Admin users
      prisma.user.count({ where: { role: "ADMIN" } }),
      // Regular users
      prisma.user.count({ where: { role: "USER" } }),
      // Recent users (last 10)
      prisma.user.findMany({
        take: 10,
        orderBy: { createdAt: "desc" },
        select: {
          id: true,
          email: true,
          name: true,
          role: true,
          createdAt: true,
        },
      }),
      // Users created this month
      prisma.user.count({
        where: {
          createdAt: {
            gte: new Date(new Date().getFullYear(), new Date().getMonth(), 1),
          },
        },
      }),
      // Users created last month
      prisma.user.count({
        where: {
          createdAt: {
            gte: new Date(
              new Date().getFullYear(),
              new Date().getMonth() - 1,
              1
            ),
            lt: new Date(new Date().getFullYear(), new Date().getMonth(), 1),
          },
        },
      }),
    ]);

    const growthRate =
      usersLastMonth > 0
        ? ((usersThisMonth - usersLastMonth) / usersLastMonth) * 100
        : usersThisMonth > 0
        ? 100
        : 0;

    return {
      stats: {
        totalUsers,
        adminUsers,
        regularUsers,
        usersThisMonth,
        usersLastMonth,
        growthRate: Math.round(growthRate * 100) / 100,
      },
      recentUsers,
    };
  } catch (error) {
    return {
      stats: {
        totalUsers: 0,
        adminUsers: 0,
        regularUsers: 0,
        usersThisMonth: 0,
        usersLastMonth: 0,
        growthRate: 0,
      },
      recentUsers: [],
    };
  }
}

